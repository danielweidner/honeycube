namespace HoneyCube.Editor.Input
{
    /// <summary>
    /// Enumerates all possible states of a mouse button.
    /// </summary>
    public enum MouseButtonState
    {
        /// <summary>
        /// The mouse button is currently pressed by the user.
        /// </summary>
        Pressed,

        /// <summary>
        /// The mouse button has been released by the user.
        /// </summary>
        Released
    }
}